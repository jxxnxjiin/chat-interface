export function generateSuccessReport(projectName?: string): string {
  return `# 🎉 프로젝트 성공 리포트

## 📊 프로젝트 개요
| 항목 | 내용 |
|------|------|
| **프로젝트명** | ${projectName || "[프로젝트명]"} |
| **기간** | 2024.12.01 ~ 2024.12.22 |
| **결과** | ✅ 성공 |

---

## ✨ 성공 요인 분석

### 1. 명확한 목표 설정
- 초기 단계에서 구체적인 KPI와 성공 기준을 정의함
- 모든 팀원이 동일한 목표를 공유함

### 2. 효과적인 AI 활용
- 적절한 프롬프트 엔지니어링으로 원하는 결과물 도출
- 반복적인 피드백을 통한 결과물 개선

### 3. 체계적인 프로세스
- 단계별 진행 상황 관리
- 정기적인 검토 및 수정

---

## 💡 추출된 프롬프트 자산

### 효과적이었던 프롬프트 패턴
\`\`\`
[역할 부여] + [구체적 맥락] + [원하는 출력 형식] + [제약 조건]
\`\`\`

---

## 📚 SOP 등록 대상

- [ ] 프로젝트 킥오프 템플릿
- [ ] AI 프롬프트 가이드라인
- [ ] 결과물 검토 체크리스트

---

*생성 시각: ${new Date().toLocaleString("ko-KR")}*
`
}

export function generateFailureReport(projectName?: string): string {
  return `# 📝 Lessons Learned 리포트

## 📊 프로젝트 개요
| 항목 | 내용 |
|------|------|
| **프로젝트명** | ${projectName || "[프로젝트명]"} |
| **기간** | 2024.12.01 ~ 2024.12.22 |
| **결과** | ❌ 실패/보류 |

---

## 🔍 실패 원인 분석 (Root Cause Analysis)

### 1. 목표 설정 문제
- **현상**: 프로젝트 범위가 명확하지 않았음
- **원인**: 초기 요구사항 분석 부족
- **영향**: 방향성 혼란, 리소스 낭비

### 2. 일정 관리 문제
- **현상**: 예정된 마감일을 지키지 못함
- **원인**: 작업량 과소 추정
- **영향**: 품질 저하, 팀 피로도 증가

### 3. 커뮤니케이션 문제
- **현상**: 이해관계자 간 기대치 불일치
- **원인**: 정기적인 싱크 미팅 부재
- **영향**: 최종 결과물 불만족

---

## 📚 Lessons Learned

### 반드시 기억해야 할 것
1. **범위 정의를 먼저, 확실하게** - Out-of-Scope을 명시적으로 정의
2. **버퍼 타임 확보** - 예상 일정의 20% 이상 여유 확보
3. **주간 체크인 필수** - 이해관계자와의 정기 미팅 설정

### 다음에는 다르게 할 것
- 프로젝트 시작 전 체크리스트 작성
- 중간 점검 마일스톤 설정
- 위험 요소 사전 식별 및 대응 계획 수립

---

## 💡 개선 권장 사항

| 영역 | 현재 | 개선 방향 |
|------|------|----------|
| 기획 | 구두 합의 | 문서화된 PRD 작성 |
| 일정 | 단일 마감일 | 단계별 마일스톤 |
| 소통 | 필요시 연락 | 정기 미팅 설정 |

---

*생성 시각: ${new Date().toLocaleString("ko-KR")}*
`
}

